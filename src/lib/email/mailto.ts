// ── mailto: URL builder ──

interface MailtoParams {
  to?: string;
  subject: string;
  body: string;
  cc?: string;
}

export function buildMailtoUrl(params: MailtoParams): string {
  const to = params.to ? encodeURIComponent(params.to) : '';
  const parts: string[] = [];
  parts.push(`subject=${encodeURIComponent(params.subject)}`);
  parts.push(`body=${encodeURIComponent(params.body)}`);
  if (params.cc) parts.push(`cc=${encodeURIComponent(params.cc)}`);
  return `mailto:${to}?${parts.join('&')}`;
}

export function openMailto(params: MailtoParams): void {
  window.location.href = buildMailtoUrl(params);
}

// ── Email content generators ──

interface EmailContent {
  subject: string;
  body: string;
}

export function getDailyLogEmailContent(
  project: Project,
  log: DailyLog
): EmailContent {
  const formattedDate = fmtDate(log.date);
  const totalWorkers = log.manpower.reduce(
    (sum, m) => sum + m.journeymanCount + m.apprenticeCount + m.foremanCount,
    0
  );

  return {
    subject: `[FieldOps] Daily Log — ${project.name} — ${formattedDate}`,
    body: [
      `DAILY LOG — ${project.name}`,
      `Date: ${formattedDate}`,
      '',
      `Weather: ${log.weather.conditions}, ${log.weather.temperature}°F, Impact: ${log.weather.impact}`,
      `Workers on site: ${totalWorkers}`,
      `Equipment: ${log.equipment.length} pieces`,
      `Work items: ${log.workPerformed.length}`,
      `RFIs: ${log.rfis.length} | Submittals: ${log.submittals.length}`,
      `Inspections: ${log.inspections.length}`,
      `Changes: ${log.changes.length}`,
      `Conflicts: ${log.conflicts.length}`,
      log.notes ? `\nNotes:\n${log.notes}` : '',
      '',
      '---',
      'Please see attached PDF for full details.',
      'Generated by FieldOps — Blackstone Construction',
    ]
      .filter(Boolean)
      .join('\n'),
  };
}

