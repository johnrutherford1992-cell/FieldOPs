export function getLegalLetterEmailContent(
  project: Project,
  letter: LegalCorrespondence,
  recipientEmail?: string
): { params: MailtoParams } {
  const typeLabels: Record<string, string> = {
    back_charge: 'Back Charge Notice',
    delay_notice: 'Delay Notice',
    cure_notice: 'Cure Notice',
    change_directive: 'Change Directive',
    rfi_followup: 'RFI Follow-Up',
    general: 'Correspondence',
  };

  const subject = `[FieldOps] ${typeLabels[letter.type] || 'Legal Correspondence'} — ${project.name}`;

  // Strip HTML tags from the generated letter for plain text body
  const plainBody = letter.generatedLetter
    ? stripHtml(letter.generatedLetter)
    : 'Please see attached PDF for the full letter.';

  // Truncate body to avoid mailto length limits
  const truncatedBody =
    plainBody.length > 1800
      ? plainBody.substring(0, 1800) + '\n\n[...truncated — see attached PDF for full text]'
      : plainBody;

  return {
    params: {
      to: recipientEmail || '',
      subject,
      body: [
        truncatedBody,
        '',
        '---',
        'Generated by FieldOps — Blackstone Construction',
      ].join('\n'),
    },
  };
}

function fmtDate(isoDate: string): string {
  return new Date(isoDate + 'T12:00:00').toLocaleDateString('en-US', {
    weekday: 'long',
    month: 'long',
    day: 'numeric',
    year: 'numeric',
  });
}

function stripHtml(html: string): string {
  return html
    .replace(/<br\s*\/?>/gi, '\n')
    .replace(/<\/p>/gi, '\n\n')
    .replace(/<\/li>/gi, '\n')
    .replace(/<li>/gi, '• ')
    .replace(/<\/h[1-6]>/gi, '\n')
    .replace(/<[^>]+>/g, '')
    .replace(/&nbsp;/g, ' ')
    .replace(/&amp;/g, '&')
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&quot;/g, '"')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}
